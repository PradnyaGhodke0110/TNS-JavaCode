package first;

public class DataTypeEx 
{
	public static void main(String args[])
	{
		int a=23;
		float f=25.5f;
		long lg=1234;
		double dl=2344.5;
		char ch='p';
		
		System.out.println("Integer number is "+a);
		System.out.println("Float number is "+f);
		System.out.println("long number is "+lg);
		System.out.println("Double number is "+dl);
		System.out.println("Character is "+ch);
		
		
	}
}
